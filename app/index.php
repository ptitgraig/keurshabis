            function readableMateriau($materiau) {
                $read = $materiau;
                switch ($materiau) {
                    case "platine":
                        $read = "Platine";
                        break;
                    case "orjaune":
                        $read = "Or Jaune";
                        break;
                    case "orblanc":
                        $read = "Or Blanc";
                        break;
                    case "orrose":
                        $read = "Or Rose";
                        break;
                    case "argent":
                        $read = "Argent";
                        break;
                }
                return $read;
            }

            // presentation des produits
            $lines = file('data/bijoux.json');
            
            $data = [];
            foreach ($lines as $line_num => $line) {
                $data[$line_num] = json_decode($line, true);
            }
            $max = sizeof($data);
            for ($i = 0; $i < $max; $i++) {
                echo '
                    <div class="square '.$data[$i]['type'].' '.$data[$i]['materiau'].' '.$data[$i]['public'].' md-trigger" data-modal="modal-'.$i.'">
                        <img src="images/bijoux/'.$data[$i]['imgname'].'" width="100" height="100" />
                        <span class="title">'.$data[$i]['title'].'</span>
                    </div>';
            }
            ?>
        </div>

